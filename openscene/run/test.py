# 输入的字符串数据
data = "4.333333333	4	3.333333333	4.666666667	3.333333333	5	3.666666667	5	5	5	5	4.333333333	3	4.333333333	4.666666667	4.666666667	5	5	4.333333333	3.666666667	1.666666667	3.333333333	3.333333333	3	2	3.333333333	3.666666667	3	3.333333333	1	2	2.666666667	2.333333333	2.666666667	3.666666667	3.666666667	2.333333333	1.666666667	2.666666667	1.333333333	3	3	2.333333333	3	2.666666667	3.333333333	2.666666667	1	2	3	3	3.666666667	2	3	3.666666667	2	2.333333333	3.333333333	3	1.333333333"

# 将数据字符串按空格分割成列表
data_list = data.split()

# 初始化奇数位和偶数位的和以及计数器
sum_odd = 0
sum_even = 0
count = 0

# 遍历列表中的元素
for i in range(len(data_list)):
    num = float(data_list[i])
    if i % 2 == 0:  # 偶数位
        sum_even += num
    else:  # 奇数位
        sum_odd += num
    count += 1

# 计算奇数位和偶数位的均值
mean_odd = sum_odd / (count // 2)
mean_even = sum_even / (count // 2)

print("偶数位的均值为:", mean_even)
print("奇数位的均值为:", mean_odd)

